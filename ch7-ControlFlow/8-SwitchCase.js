
// This is switch - case Examples 

let fruit=1;

switch(fruit){
    case 1:
        console.log("apple");
        break;

    case 2:
        console.log("banana");
        break;

     case 3:
            console.log("mango");
        break;

     case 4:
            console.log("grapes");
       break;

     default:
        console.log("invalid fruit");
}

